public class MathExample {
    public static void main(String[] args) {
        System.out.println(2-(4-4)+ 7+8*5);
        System.out.println(122-118-1+44*3+5/2%5);
        System.out.println(-5+5);
        System.out.println(6*((-2+3) * (2-1)));  //6
        System.out.println(6*-2+3*(2-1)); //-9
        System.out.println(6*(-2+3) * (2-1));//6
        System.out.println(6*-2+3*2-1); //-7
        System.out.println((5.3+7)/2.0);
        System.out.println(5.3+7/2.0);
        System.out.println((5.3+7)/2);
        System.out.println(3*2%4/5.0*2*3);
    }
}
